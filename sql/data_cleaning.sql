-- cleaning runner_orders table

UPDATE runner_orders
SET pickup_time = NULL
WHERE pickup_time = 'null';

ALTER TABLE runner_orders
MODIFY COLUMN pickup_time timestamp;


-- changing distance column

UPDATE runner_orders
set distance = CASE
WHEN distance LIKE '%null%' THEN NULL
WHEN distance LIKE '%km%' THEN trim(replace(distance, 'km', ''))
ELSE trim(distance)
end;

ALTER TABLE runner_orders
modify COLUMN distance decimal(4,1);

-- changing duration column

UPDATE runner_orders
SET duration = CASE
WHEN duration LIKE '%null%' THEN NULL
WHEN duration LIKE '%mins%' THEN trim(replace(duration, 'mins', ''))
WHEN duration LIKE '%minutes%' then trim(replace(duration, 'minutes', ''))
WHEN duration LIKE '%minute%' THEN trim(replace(duration, 'minute', ''))
ELSE trim(duration)
end;

ALTER TABLE runner_orders
MODIFY COLUMN duration integer;

UPDATE runner_orders
SET duration = sec_to_time(duration * 60)
WHERE duration IS NOT NULL;

ALTER TABLE runner_orders
MODIFY COLUMN duration time;

-- cancellation column

UPDATE runner_orders
SET cancellation = NULL
WHERE cancellation = 'null';

UPDATE runner_orders
SET cancellation = NULL
WHERE cancellation = '';

